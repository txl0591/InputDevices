/*************************************************
 Function:		FontGetAccsiiIndex
 Descroption:	 
 Input: 
	1.value
	2.font
 Output: 
 Return: 	
 Other:  
*************************************************/
static int FontGetAccsiiIndex(char value, FontType font)
{
    int Index = 0;
    if(value >= '0' && value <= '9')
    {
        Index = value-'0';
    }
    else
    {
        if(value >= 'A' && value <= 'Z')
        {
            Index = value-'A'+10;
        }
        else
        {
            switch(value)
            {
                case '_':
                    if(font == Font12x8)
                    {
                        Index = 36;
                    }
                    else
                    {
                        Index = 10;
                    }
                    break;

                case '*':
                    Index= 37;
                    break;  

                case '#':
                    Index= 38;
                    break;

                case '/':
                    Index= 39;
                    break;  

                case '?':
                    Index= 40;
                    break; 

                case '!':
                    Index= 41;
                    break; 
                    
                case '(':
                    Index= 42;
                    break; 

                case ')':
                    Index= 43;
                    break;    

                 case ':':
                    Index= 44;
                    break;

                 case '-':
                    Index= 45;
                    break;
            }
        }
    }

    
    return Index;
}

